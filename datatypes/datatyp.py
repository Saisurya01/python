# in python, The datatypes are defines the type of data that can be stored in a variable. 
# The data types in python are declared mannually.
name="sai surya"
print(type(name)) # string

roll=14
print(type(roll)) # integer

weight=63.5
print(type(weight)) # float

is_student=True
print(type(is_student)) # boolean

my_list=[1,2,3,4]
print(my_list)
print(type(my_list)) # list is a collection of items that are ordered and changeable. It allows duplicate members.

my_tuple=(1,2,3,4)
print(my_tuple)
print(type(my_tuple)) # tuple is a collection of items that are ordered and unchangeable. It allows duplicate members.

my_dict={"name":"sai surya","age":19,"roll":14}
print(my_dict)  
print(type(my_dict)) # dictionary is a collection of items that are unordered, changeable, and indexed. It does not allow duplicate members.

my_set={1,2,3,4}
print(my_set)
print(type(my_set)) # set is a collection of items that are unordered, unchangeable, and unindexed. It does not allow duplicate members.