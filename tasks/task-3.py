print("Voting Eligibility Checker")
age=int(input("Enter your age: "))
if age>=18:
    print("You can vote ✅")
else:
    print("Sorry, you're not eligible ❌")
# This code checks if a person is eligible to vote based on their age.
# If the age is 18 or older, it prints that the person is eligible.
# If the age is less than 18, it prints that the person is not eligible.