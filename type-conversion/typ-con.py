# type converstion is very useful in python to convert one data type to another data type.
age=int(input("Enter your age: "))  # input is always string
print(f"your age is {age} and type of age is {type(age)}")

height=float(input("Enter your height in cm: "))  # input is always string
print(f"your height is {height} and type of height is {type(height)}")
